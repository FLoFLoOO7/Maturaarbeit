def converter(x):
    for i in range(6):
        for z in range(9):
            if z == 4:
                continue  # Spalte 4 überspringen

            r, g, b = x[i][z]

            # ROT
            if 30 <= r <= 45 and 20 <= g <= 35 and 20 <= b <= 40:
                x[i][z] = "Rot"

            # GRÜN
            elif 5 <= r <= 10 and 30 <= g <= 45 and 60 <= b <= 80:
                x[i][z] = "Gre"

            # BLAU
            elif 3 <= r <= 8 and 15 <= g <= 25 and 50 <= b <= 65:
                x[i][z] = "Blu"

            # ORANGE oder WEISS (b == 100 bei beiden)
            elif b == 100:
                if r + g >= 140:
                    x[i][z] = "Wei"
                else:
                    x[i][z] = "Ora"

            # GELB – fallback
            else:
                x[i][z] = "Gel"

    return x
